package logic;



/**
 * @author chaitanya
 */
public class GeneSolution {
    int MachineID;
    int time;
   

    /**
     *
     * @param MachineID
     * @param time
     */
    public GeneSolution(int MachineID, int time) {
        
        this.MachineID = MachineID;
        this.time = time;
    }
    
   

   
    /**
     *
     * @param MachineID
     */
    public void setMachineID(int MachineID) {
        this.MachineID = MachineID;
    }

    /**
     *
     * @param time
     */
    public void setTime(int time) {
        this.time = time;
    }



    /**
     *
     * @return
     */
    public int getMachineID() {
        return MachineID;
    }

    /**
     *
     * @return
     */
    public int getTime() {
        return time;
    }
}
